#include "math_function.hh"

namespace Math {

double mbledhja(double numri1, double numri2) { return numri1 + numri2; }

double zbritja(double numri1, double numri2) { return numri1 - numri2; }

double prodhimi(double numri1, double numri2) { return numri1 * numri2; }

double pjestimi(double numri1, double numri2) { return numri1 / numri2; }

}